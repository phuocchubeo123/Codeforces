double H, U, D, F;
void solve(){
    int day = 0;
    double h = 0;
    double d = U * F / 100;
    while (true){
        day++;
        // cout << "day: " << day << " " << h << " " << U << "\n";
        if (U <= 0) U = 0;
        h += U;
        if (h > H){
            cout << "success on day " << day << "\n";
            return;
        }
        h -= D;
        U -= d;
        if (h < 0){
            cout << "failure on day " << day << "\n";
            return;
        }
    }
}

